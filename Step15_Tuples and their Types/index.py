#Tuple
my_tuple = (1, "Hello", True)
print(my_tuple[0])
print(my_tuple[1]) 
print(my_tuple[2]) 

#Types of Tuples
#Empty Tuple
empty_tuple = ()
print(empty_tuple)  

#Single Element Type
single_tuple = (5,)  
print(single_tuple)  

#Type With Multiple Elements
multi_tuple = (10, 20, 30)  
print(multi_tuple) 

#Tuple with Different Data Types 
mixed_tuple = (1, "Hello", 3.14, True)  
print(mixed_tuple)  

#Nested Tuple
nested_tuple = (1, (2, 3), (4, 5, 6))  
print(nested_tuple)  

#Tuple With Duplicates
duplicate_tuple = (1, 2, 2, 3, 4, 4, 5)  
print(duplicate_tuple)  

#Tuple With Range
range_tuple = tuple(range(1, 6))  
print(range_tuple)
