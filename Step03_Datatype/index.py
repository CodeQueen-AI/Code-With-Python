# int (integer)
age = 25
print("Integer:", age) 

# float (decimal)
pi = 3.14
print("Float:", pi)  

# str (string)
name = "Code Queen"
print("String:", name) 

# bool (boolean)
is_active = True
print("Boolean:", is_active)  

# list
fruits = ["apple", "banana", "mango"]
print("List:", fruits) 

# tuple
coordinates = (10, 20)
print("Tuple:", coordinates)  

# dict (dictionary)
person = {"name": "Sumbal", "role": "Code Queen"}
print("Dictionary:", person)  

# set
unique_numbers = {1, 2, 3, 3}
print("Set:", unique_numbers)  

# NoneType
value = None
print("NoneType:", value)  
